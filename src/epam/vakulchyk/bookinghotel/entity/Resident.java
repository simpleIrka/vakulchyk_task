package epam.vakulchyk.bookinghotel.entity;

public class Resident {
    private int coast;
    private long idOrder;
    private int numderRoom;

    public int getCoast() {
        return coast;
    }

    public void setCoast(int coast) {
        this.coast = coast;
    }

    public long getIdOrder() {
        return idOrder;
    }

    public void setIdOrder(long idOrder) {
        this.idOrder = idOrder;
    }

    public int getNumderRoom() {
        return numderRoom;
    }

    public void setNumderRoom(int numderRoom) {
        this.numderRoom = numderRoom;
    }
}
